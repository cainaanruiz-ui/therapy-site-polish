export const updateOrder = createServerFn({ method: "POST" })
  .middleware([requireSupabaseAuth])
  .inputValidator((input: unknown) =>
    z
      .object({
        id: z.string().uuid(),
        status: z.enum(["pending", "paid", "fulfilled", "shipped", "cancelled", "refunded"]).optional(),
        tracking_number: z.string().max(255).optional().nullable(),
        carrier: z.string().max(100).optional().nullable(),
        notes: z.string().max(5000).optional().nullable(),
      })
      .parse(input),
  )
  .handler(async ({ data, context }) => {
    await assertAdmin(context.userId);
    const { supabaseAdmin } = await import("@/integrations/supabase/client.server");
    const { id, ...rest } = data;
    const { error } = await supabaseAdmin.from("orders").update(rest).eq("id", id);
    if (error) throw new Error(error.message);
    return { ok: true };
  });